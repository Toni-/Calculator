public class Functions {
	
	public int addition(int a, int b) {
		int sum = a + b;
		
		return sum;
	}
	
	public int division(int a, int b) {
		int quotient = a / b;
		
		return quotient;
	}
	
	public int multiplication(int a, int b) {
		int product = a * b;
		
		return product;
	}
	
	public int subtraction(int a, int b) {
		int difference = a - b;
		
		return difference;
	}
}
